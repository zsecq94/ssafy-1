def count_vowels(a):
    count = 0
    for i in a:
        if i.count('a'):
            count += 1
        elif i.count('e'):
            count += 1
        elif i.count('i'):
            count += 1
        elif i.count('o'):
            count += 1
        elif i.count('u'):
            count += 1
    return count
